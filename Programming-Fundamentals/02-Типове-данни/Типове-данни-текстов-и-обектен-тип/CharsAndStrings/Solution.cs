    /* 17. Низове и знаци
    Напишете програма, която задава знакови и низвои стойности на променливи. 
    Бъдете сигурни, че всяка стойност се съхраява в коректната променлива. 
    Накрая, трябва да изпечатаме всички променливи на конзолата.
    */
    class Solution
    {
        static void Main(string[] args)
        {
            string first = Console.ReadLine();
            string second = Console.ReadLine();
            string third = Console.ReadLine();
            string fourth = Console.ReadLine();
            string fifth = Console.ReadLine();
            Console.WriteLine(first);
            Console.WriteLine(second);
            Console.WriteLine(third);
            Console.WriteLine(fourth);
            Console.WriteLine(fifth);
        }
    }
